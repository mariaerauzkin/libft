#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*str;

	str = "0123456789";
	if (n == -2147483648)
	{
		write (fd, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
	{
		write(fd, &str[n], 1);
	}
}
