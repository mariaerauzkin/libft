#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	ind;
	size_t	j;

	ind = 0;
	j = 0;
	while (src[ind] != '\0')
		ind++;
	if (dstsize == 0)
		return (ind);
	while (src[j] != '\0' && j < dstsize - 1)
	{
		dst[j] = src[j];
		j++;
	}
	dst[j] = '\0';
	return (ind);
}
